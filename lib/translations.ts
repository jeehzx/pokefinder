// Função para traduzir texto
export function translate(text: string): string {
  return translations[text as keyof typeof translations] || text;
}

// Função para traduzir nome de stat
export function translateStatName(statName: string): string {
  const normalized = statName.toLowerCase().replace(/\s+/g, '-');
  return translate(normalized) || statName;
}

// Função para traduzir egg group
export function translateEggGroup(group: string): string {
  const normalized = group.toLowerCase().replace(/\s+/g, '-');
  return translate(normalized) || group;
}

// Função para traduzir nome de habilidade
export function translateAbilityName(ability: string): string {
  const normalized = ability.toLowerCase().replace(/\s+/g, '-');
  const translated = translate(normalized);
  // Se encontrou tradução, retorna. Caso contrário, formata o nome original
  if (translated !== normalized) {
    return translated;
  }
  return ability
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(' ');
}

// Função para traduzir tipo de Pokémon
export function translateType(type: string): string {
  const normalized = type.toLowerCase();
  return translate(normalized) || type;
}

// Função para traduzir nome de item
export function translateItemName(itemName: string): string {
  const normalized = itemName.toLowerCase().replace(/\s+/g, '-');
  return translate(normalized) || formatItemName(itemName);
}

// Função auxiliar para formatar nome de item
function formatItemName(itemName: string): string {
  return itemName
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(' ');
}

